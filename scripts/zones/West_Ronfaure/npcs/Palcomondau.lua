end

function onPath(npc)
    local reported = false
    
    if npc:atPoint(path[50][1], path[50][2], path[50][3]) then
        if npc:getLocalVar("[REPORT_DONE]") == 0 then
            npc:showText(npc, ID.text.PALCOMONDAU_REPORT)
            npc:setLocalVar("[REPORT_DONE]",1)
        end 
